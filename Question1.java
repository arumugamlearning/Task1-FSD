package TASK1;

public class Question1
{

    public static void main(String[] args)
    {
        //Using For Loop
        for(int i=10;i<=50;i++)
        {
            System.out.println(i);
        }
        //Using while loop
        int j=10;
        while (j<=50)
        {
            System.out.println(j);
            j++;
        }
    }

}
